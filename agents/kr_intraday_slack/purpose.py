"""장중 Slack 파이프라인 — 단타·짧은 매매 판단 보조 목적 (에이전트 공통)."""

from __future__ import annotations

APP_PURPOSE = """\
[앱 목적 — 단타·짧은 매매 판단 보조]
이 파이프라인은 장기 투자 리포트가 아닙니다.
장중에 빠르게 돈이 들어오는 종목을 찾고, 1주 단위라도 진입 타이밍을 테스트할 수 있게 돕는
단타 실전 판단 보조 신호를 만듭니다."""

COMMON_PRINCIPLES = """\
[공통 원칙]
1. 예쁜 설명보다 실전 판단 우선 — 지금 볼 만한가 / 어디서 볼까 / 언제 넘길까
2. 추격매수 위험이면 발송하지 않음
3. 진입 후보 구간 없으면 발송하지 않음
4. 경고(무효·회피 기준)가 명확하지 않으면 발송하지 않음
5. 애매하면 보내지 않음
6. Slack은 10초 안에 이해 가능하게
7. 기업 소개·장황한 섹터 해설·단타에 무관한 뉴스 요약은 줄임
8. "매수하세요", "무조건", "급등 확실" 금지 — "진입 후보 구간", "경고", "1주 기준", "오늘은 넘기기" 사용"""

MARKET_DATA_PURPOSE = """\
[MarketDataAgent]
단타 판단에 필요한 장중 데이터만 빠르게 수집한다.
우선: 현재가, 당일 고·저, 전일 종가, 거래대금·거래대금 변화, 외국인/기관 수급, 52주 고점 대비 위치.
단타에 직접 도움 안 되는 항목은 우선순위를 낮춘다."""

SECTOR_MOOD_PURPOSE = """\
[SectorMoodAgent]
섹터 설명이 아니라 "오늘 돈이 들어오는 섹터인지"만 strong/neutral/weak로 판단한다.
strong → 후보 가중치 상향, weak → 후보를 더 엄격히 걸러냄.
기준: 섹터 내 동반 움직임, 거래대금 증가, 1~2종목만 튀는지 vs 전체 흐름, 이슈가 수급으로 이어지는지."""

WATCHLIST_PICK_PURPOSE = """\
[WatchlistPickAgent]
관심 25개 중 "오늘 장중 진입 가능성"이 있는 종목만 1차 후보로 올린다.
우선: 거래대금 증가, 고점 대비 과도한 추격 아님, 섹터 흐름 alive, 수급 급격 악화 아님.
제외: 거래대금 부족, 수급 약함, 추격 위험, 진입 구간 산정 불가, 데이터 부족, 판단 애매."""

ENTRY_PRICE_PURPOSE = """\
[EntryPriceAgent]
반드시 "진입 후보 구간"(1주 기준 매수 타이밍 노릴 가격대)을 만든다.
경고는 매수가가 아니라 그 구간이 무효화되는 회피 기준("이탈·거래 급감 시 오늘은 넘기기").
진입 구간이 현재가보다 높거나 추격 구간이면 발송 대상에서 제외한다."""

DEEPSEEK_JUDGE_PURPOSE = """\
[DeepSeek Judge]
단타 실전성으로 send_slack 최종 결정. 애매·추격·구간·경고 없으면 false.
기업 IR식 설명 금지. JSON만 응답."""

GROK_PURPOSE = """\
[Grok Social Agent]
발송 판단 변경 금지. send_slack=true 종목만.
"왜 지금 이 종목이 언급되는지" 1~2완성문장만. 긴 뉴스·모호한 기대감·줄임표 금지."""

GEMINI_POLISH_PURPOSE = """\
[Gemini Polish Agent]
10초 안에 읽히게 말투만 다듬음. 구조(섹터·종목·현재가·진입 후보 구간·1주 기준·경고) 절대 유지.
"취소 조건" 금지 → "경고". 테스트/드라이런/검증 본문 금지."""

SEND_FILTER_PURPOSE = """\
[SendFilterAgent]
단타에 도움 되는 알림만. 진입 구간·경고 없음·추격·애매 → 제외.
당일 재발송은 진입 구간·decision·경고·수급 등 의미 있는 변화가 있을 때만."""
